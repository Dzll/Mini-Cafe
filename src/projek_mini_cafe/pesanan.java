    private void tamakActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_tamakActionPerformed
        // TODO add your handling code here:
        boolean ceknul = false;
        if(lbmakan.getText().equals("")){
            ceknul = true;
            JOptionPane.showMessageDialog(null, "Pilih Menu Makanan!!","WARNING!!", JOptionPane.ERROR_MESSAGE);
        }
        if(txjumlahmakan.getText().trim().equals("")){
            ceknul = true;
            txjumlahmakan.setBackground(Color.red);
            JOptionPane.showMessageDialog(null, "Input Jumlah pemesanan !!","WARNING!!", JOptionPane.ERROR_MESSAGE);
        }
        
        int totmm = Integer.parseInt(hrgm) * Integer.parseInt(txjumlahmakan.getText());
        
        if(!ceknul){
            String li = String.valueOf(tbmenumakan.getValueAt(tbmenumakan.getSelectedRow(), 1));  

            DefaultTableModel mod = (DefaultTableModel)tbpesanmakan.getModel();
            mod.addRow(new Object[]{
                txmakan.getText(),li, txjumlahmakan.getText(), totmm
            });
            pemakHiden();
            tablemenuMakan(tbmenumakan);
            lbmakan.setText("");
            txjumlahmakan.setText("");
            tamak.setVisible(false);
        }
    }//GEN-LAST:event_tamakActionPerformed

