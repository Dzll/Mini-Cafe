    public int tot(){
        int hargaCount = tbpesanmakan.getRowCount();
        int sum = 0;
        for(int u = 0; u < hargaCount; u++){
            sum = sum+Integer.parseInt(tbpesanmakan.getValueAt(u, 3).toString());
        }
        return sum;
    }
    public int tot2(){
        int hargaCount2 = tbpesanminum.getRowCount();
        int sum2 = 0;
        for(int u = 0; u < hargaCount2; u++){
            sum2 = sum2+Integer.parseInt(tbpesanminum.getValueAt(u, 3).toString());
        }
        return sum2;
    }
    
